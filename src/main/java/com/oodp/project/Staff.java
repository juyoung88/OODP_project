package com.oodp.project;

public class Staff {
    private String staffID;
    private String staffName;
    private String role;
    private int salary;

    public Staff(String staffID, String staffName, String role, int salary) {
        super();
        this.staffID = staffID;
        this.staffName = staffName;
        this.role = role;
        this.salary = salary;
    }
}